#include <iostream>

using namespace std;

int main()
{
    int a, b,k,n, sum = 0;
    int arr[n];
    cin>>k>>n;
    for(int i = 0; i<k; i++)
    {
        arr[i];
    }
    for(int i=0; i<n; i++)
    {
        cin>>a>>b;
        for(int j =a; j<=b; j++)
        {
            sum = sum+arr[j];
        }
        cout<<sum<<endl;
    }
    

    return 0;
}
